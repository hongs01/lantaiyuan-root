package com.lantaiyuan.bean;

public class StationObject {

	private String stationId;
	
	private String stationNo;
	
	private String planningTime;
	
	private String predictedTime;
	
	private String realTime;

	public String getStationId() {
		return stationId;
	}

	public void setStationId(String stationId) {
		this.stationId = stationId;
	}

	public String getStationNo() {
		return stationNo;
	}

	public void setStationNo(String stationNo) {
		this.stationNo = stationNo;
	}

	public String getRealTime() {
		return realTime;
	}

	public void setRealTime(String realTime) {
		this.realTime = realTime;
	}

	public String getPredictedTime() {
		return predictedTime;
	}

	public void setPredictedTime(String predictedTime) {
		this.predictedTime = predictedTime;
	}

	public String getPlanningTime() {
		return planningTime;
	}

	public void setPlanningTime(String planningTime) {
		this.planningTime = planningTime;
	}
	
	
	
	
}
